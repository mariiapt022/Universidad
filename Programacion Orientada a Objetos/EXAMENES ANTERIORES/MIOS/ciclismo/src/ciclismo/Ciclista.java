package ciclismo;

public class Ciclista {
	private String nombre,equipo;
	private int dorsal;
	
	public Ciclista(String n,String e,int d) {
		nombre=n;
		equipo=e;
		dorsal=d;
	}

	public String getNombre() {
		return nombre;
	}

	public String getEquipo() {
		return equipo;
	}

	public int getDorsal() {
		return dorsal;
	}


	
	
	
	
	
}
